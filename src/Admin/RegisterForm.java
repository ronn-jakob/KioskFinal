    PlainDocument pd2 = new PlainDocument(){
        int min = 8 ;
        @Override
        public void insertString(int offset, String atr, AttributeSet attr)  
            throws BadLocationException {
                passWarning.setText("");
                if (atr.contains(" ")) {
                    JOptionPane.showMessageDialog(null, "Spaces are not allowed in password.");
                    atr = atr.replace(" ", "");
                }
                if(getLength() + atr.length() < min){
                    passWarning.setText("Atleast 8 Characters Required");
                }             
                super.insertString(offset, atr, attr);
            }
        @Override
        public void remove(int offset, int length)
            throws BadLocationException {
                super.remove(offset, length);         
                if (getLength() < 8) {
                    passWarning.setText("Atleast 8 Characters Required");
                }
                 else {
                    passWarning.setText("");
                }
                if(getLength() == 0){
                    passWarning.setText(" ");
                }
            }
        };
