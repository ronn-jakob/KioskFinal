    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String username = usernameTextField.getText().trim();
        String password = new String(passField.getPassword());
        String conPass = new String(conPassField.getPassword());
        String role;

        if (username.endsWith(".admin")) {
            role = "admin";
            username = username.replace(".admin", ""); // optional: remove suffix
        } else {
            role = "cashier";
        }

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter your username.");
        }
        else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter your password.");
        }
        else if (conPass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please confirm your password.");
        }
        else if (password.length() < 8) {
            JOptionPane.showMessageDialog(null, "Password must be at least 8 characters long.");
        }
        else if (!password.equals(conPass)) {
            JOptionPane.showMessageDialog(null, "Password did not match.");
        }
        else {
            try {
                String checkQuery = "SELECT * FROM users WHERE username = ?";
                PreparedStatement checkStmt = conn.prepareStatement(checkQuery);
                checkStmt.setString(1, username);

                ResultSet rs = checkStmt.executeQuery();

                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Username already exists.");
                    return;
                }

                String insertQuery = "INSERT INTO users (username, password, role) VALUES (?, ?, ?)";
                PreparedStatement pst = conn.prepareStatement(insertQuery);
                pst.setString(1, username);
                pst.setString(2, password);
                pst.setString(3, role);

                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Successfully Registered!");

                usernameTextField.setText("");
                passField.setText("");
                conPassField.setText("");

                new LoginForm().setVisible(true);
                RegisterForm.this.dispose();

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Database Error!");
            }
        }
    }//GEN-LAST:event_loginButtonActionPerformed

